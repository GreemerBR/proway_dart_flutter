class Calculator {
  var number1 = 10.0;
  var number2 = 5.0;
  var result = 0.0;

  void sum() {
    result = number1 + number2;
  }

  void sub() {
    result = number1 - number2;
  }

  void multiply() {
    result = number1 * number2;
  }

  void divide() {
    result = number1 / number2;
  }
}
